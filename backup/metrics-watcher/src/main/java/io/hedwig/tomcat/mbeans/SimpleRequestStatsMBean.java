public class SimpleRequestStatsMBean implements RequestStatusMBean {

    public void updateStats(long timestamp, ServletRequest request, long elapsed) {
        totalElapsedTime.addAndGet(elapsed);
        _requestCount.incrementAndGet();
    }

    @Override
    public long getProcessingTime() {
        return _totalElapsedTime.get();
    }

    public long getRequestCount() {
        return _requestCount.get();
    }

    @Override
    public void resetCounters() {
        _totalElapsedTime.set(0l);
        _requestCount.set(0l);
    }
}

